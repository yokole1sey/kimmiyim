function countPastEclipses(eclipseArray, birthDate, today) {
  return eclipseArray.filter(function(e) {
    var d = new Date(e[0], e[1] - 1, e[2]);
    return d > birthDate && d <= today;
  }).length;
}

function countFutureEclipses(eclipseArray, today, deathDate) {
  return eclipseArray.filter(function(e) {
    var d = new Date(e[0], e[1] - 1, e[2]);
    return d > today && d <= deathDate;
  }).length;
}
